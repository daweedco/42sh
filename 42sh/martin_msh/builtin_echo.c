#include "ft_minishell.h"
#include "libft.h"

int	msh_echo(char **req, char ***env)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	(void)env;
	if ((req[1] != NULL) && !(ft_strcmp(req[1], "-n")))
	{
		n++;
		i++;
	}
	while (req[++i])
	{
		if (!(((i == 2) && (n == 1)) || ((i == 1) && (n == 0))))
			ft_putstr(" ");
		ft_putstr(req[i]);
	}
	if (!n)
		ft_putstr("\n");
	return (0);
}
